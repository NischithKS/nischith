package imp_questions_java_programing;



	// Multiplication Program (print multiplication table for a number)

public class A18 {
	    public static void main(String[] args) {
	        int number = 5;  // hardcoded number
	        for (int i = 1; i <= 10; i++) {
	            System.out.println(number + " x " + i + " = " + (number * i));
	        }
	    }
	}